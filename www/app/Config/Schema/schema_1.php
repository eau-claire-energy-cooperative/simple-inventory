	public function after($event = array()) {
		
		if(isset($event['update']))
		{
			switch ($event['update']){
				case 'commands':

					$command = ClassRegistry::init('Command');
					
					//get all the command objects
					$allCommands = $command->find('all');
					
					foreach($allCommands as $aCommand){
						
						$command->create();
						if($aCommand['Command']['id'] == 1)
						{
							$command->query(sprintf("update commands set description = '%s' where id = %d",'Alert system administrators of any programs currently installed that have been flagged as Restricted in the Programs area.',$aCommand['Command']['id']));
						}
						else if($aCommand['Command']['id'] == 2)
						{
							$command->query(sprintf("update commands set description = '%s' where id = %d",'Wake a specific computer via a WOL packet at a given time. ',$aCommand['Command']['id']));

						}
						else if($aCommand['Command']['id'] == 3)
						{
							$command->query(sprintf("update commands set description = '%s' where id = %d",'Shutdown a computer by computer name at a given time. Please note that computers that are sleeping or hibernating will be left alone.',$aCommand['Command']['id']));
							
						}	
						else if($aCommand['Command']['id'] == 4)
						{
							$command->query(sprintf("update commands set description = '%s' where id = %d",'This command should be kept running at all times. It will clear the email queue by sending emails to system administrators.',$aCommand['Command']['id']));
					
						}	
						else if($aCommand['Command']['id'] == 5)
						{
							$command->query(sprintf("update commands set description = '%s' where id = %d",'Check the disk space available on all computers. Any that do not contain the minimum amount of space (in percent) will generate an email to the system administrator. ',$aCommand['Command']['id']));
						}	
						else if($aCommand['Command']['id'] == 6)
						{
							$command->query(sprintf("update commands set description = '%s' where id = %d",'Send a restart command to a given computer at a specific time. ',$aCommand['Command']['id']));
						}		
					}

				break;
			}
		}
	}

